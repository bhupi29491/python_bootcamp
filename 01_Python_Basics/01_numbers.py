# Basic Maths
print("Basic Maths:")
print("2 + 1 = ", 2 + 1)
print("2 - 1 = ", 2 - 1)
print("2 * 2 = ", 2 * 2)
print("3 / 2 = ", 3 / 2)

#  Modulo or "Mod" Operator --> This returns a remainder
print("Mod Operator:")
print("7 % 4 = ", 7 % 4)
print("50 % 5 = ", 50 % 5)
print("23 % 2 = ", 23 % 2)

# Power operator
print("Power Operator:")
print("2 ** 3 = ", 2 ** 3)

#  Calculations
print("2 + 10 * 10 + 3 = ", 2 + 10 * 10 + 3)
print("(2 + 10) * (10 + 3) = ", (2 + 10) * (10 + 3))

# To assign the value to a variable
a = 5
print("The value of a = ", a)
print("The value of a + a = ", a + a)

a = a + a
print("After reassignment of variable a = ", a)

#  To check the typeof a variable
print(type(a))

a = 30.1
print(type(a))

