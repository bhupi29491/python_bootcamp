# and operator : All the conditions need to be true
print((1 < 2) and 'Bhupi' == 'Bhupi')  # O/P : True
print((1 < 2) and 'Bhupi' == 'Bhupss')  # O/P : False

# or operator : Any one condition need to be true
print((1 < 2) or 'Bhupi' == 'Bhupss')  # O/P : True
print((100 == 2) or (2 == 2))  # O/P : True

# not operator : All the conditions need to be true
print(not (1 == 1))  # O/P : False
print(not 400 > 500)  # O/P : True
